//operator perbandingan/relasional
#include <iostream>
using namespace std;

int main() {
    int a = 27;
    int b = 11;
    
    cout <<(a > b)<<endl; //lebih besar
    cout <<(a < b)<<endl; //lebih kecil
    cout <<(a == b)<<endl;
    cout <<(a != b)<<endl;
    cout <<(a >= b)<<endl;
    cout <<(a <= b)<<endl;
    
    return 0;
}