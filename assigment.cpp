//operator assigment
#include <iostream>
using namespace std;

int main() {
    int a = 10;
    
    a +=7; // a = a + 7
    cout <<a << endl;
    
    a-=6; //a = a - 6
    cout <<a <<endl;
    
    //operator assigment digunakan
    //untuk memberikan nilai ke sebuah variabel
    // bacanya dari kanan ke kiri
    
    return 0;
}

